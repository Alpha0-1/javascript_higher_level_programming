// Demonstrates converting values to integers

const stringValue = "1234";
const numberValue = 56.78;
const booleanValue = true;

// Convert to integers using Number() and parseInt()
console.log("String to integer:", parseInt(stringValue)); // 1234
console.log("Number to integer:", Number(numberValue)); // 56
console.log("Boolean to integer:", Number(booleanValue)); // 1
